// djb2 字串雜湊：把問題轉成穩定整數，讓「同問同卜得同答」
function hashStr(s) {
  let h = 5381;
  const str = s || '';
  for (let i = 0; i < str.length; i++) h = (((h * 33) ^ str.charCodeAt(i)) >>> 0);
  return h;
}

