function drawFortune() {
  const totalW = FORTUNES.reduce((a, f) => a + f.w, 0);
  let r = Math.random() * totalW;
  for (const f of FORTUNES) {
    if (r < f.w) return f;
    r -= f.w;
  }
  return FORTUNES[0];
}

