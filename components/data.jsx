// 時辰 from hour (0-23)
function getShichen(hour) {
  return Math.floor(((hour + 1) % 24) / 2) % 12;
}

// 簡化的日干支（以 2000-01-01 為 庚辰 / 第 16 組甲子）
function getDayGanZhi(date) {
  const anchor = new Date('2000-01-01T00:00:00+08:00');
  const days = Math.floor((date - anchor) / 86400000);
  // 2000-01-01 is 庚辰 day → gan=6, zhi=4
  const gan = ((days + 6) % 10 + 10) % 10;
  const zhi = ((days + 4) % 12 + 12) % 12;
  return { gan, zhi, label: GAN[gan] + ZHI[zhi] };
}

// 農曆月（粗略對照節氣月）
function getYueZhi(date) {
  return YUE_ZHI[date.getMonth()];
}

// 梅花易數時辰起卦
function meihua(date, seedAdd = 0) {
  const y = date.getFullYear() + seedAdd;
  const m = date.getMonth() + 1;
  const d = date.getDate();
  const sc = getShichen(date.getHours()) + 1; // 1-12
  const upper = ((y + m + d) % 8 + 8) % 8;
  const lower = ((y + m + d + sc) % 8 + 8) % 8;
  const movingYao = ((y + m + d + sc) % 6 + 6) % 6 + 1;
  return { upper, lower, movingYao };
}

// 幸運數字（1-9）
function numerology(date, dirIdx) {
  const n = date.getFullYear() + date.getMonth() + 1 + date.getDate() + date.getHours() + dirIdx;
  let s = n.toString().split('').reduce((a,c)=>a+Number(c),0);
  while (s > 9) s = s.toString().split('').reduce((a,c)=>a+Number(c),0);
  return s || 9;
}

// 塔羅牌 draw with reverse
function drawTarot(seed) {
  const idx = Math.abs(Math.floor(seed * 1000)) % TAROT.length;
  const rev = (Math.floor(seed * 10000) % 10) < 3; // 30% 逆位
  return { ...TAROT[idx], reversed: rev };
}

