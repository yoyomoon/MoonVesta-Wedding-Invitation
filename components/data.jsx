// 用問題雜湊 + 時間 + 方位 當種子，與梅花易數的「時空之卦」分開——這是「你親手卜出的問卦」
// 八卦 binary 對應 TRIGRAMS index：乾0 兌1 離2 震3 巽4 坎5 艮6 坤7
//   bit2=top bit1=middle bit0=bottom (1=yang, 0=yin)
const _TRIGRAM_FROM_BITS = { 7:0, 3:1, 5:2, 1:3, 6:4, 2:5, 4:6, 0:7 };
function liuyao(seedNum) {
  let s = (seedNum >>> 0) || 1;
  const yaos = [];
  // 用 LCG 從 seed 衍生 6 個獨立爻值；每爻 v ∈ {6 老陰, 7 少陽, 8 少陰, 9 老陽}
  for (let i = 0; i < 6; i++) {
    s = (Math.imul(s, 1664525) + 1013904223) >>> 0;
    const r = s / 4294967295; // 0..1
    let v;
    if (r < 0.125)      v = 6; // 老陰 ▒▒  動 (3 tails)
    else if (r < 0.500) v = 8; // 少陰 ─ ─ 靜
    else if (r < 0.875) v = 7; // 少陽 ─── 靜
    else                v = 9; // 老陽 ─── 動 (3 heads)
    yaos.push(v);
  }
  const isYang = v => (v === 7 || v === 9) ? 1 : 0;
  const isMoving = v => (v === 6 || v === 9);
  const flipped = v => (v === 6 ? 7 : v === 9 ? 8 : v); // 動爻變反
  // bottom→top: yaos[0]=初爻, yaos[5]=上爻
  // 下卦 = yaos[0..2], 上卦 = yaos[3..5]
  const triFromYaos = (arr3) => _TRIGRAM_FROM_BITS[(isYang(arr3[2])<<2) | (isYang(arr3[1])<<1) | isYang(arr3[0])];
  const benLower = triFromYaos(yaos.slice(0, 3));
  const benUpper = triFromYaos(yaos.slice(3, 6));
  const movingIdxs = yaos.map((v, i) => isMoving(v) ? i + 1 : null).filter(x => x !== null);
  let zhiLower = benLower, zhiUpper = benUpper;
  if (movingIdxs.length > 0) {
    const zhiYaos = yaos.map(flipped);
    zhiLower = triFromYaos(zhiYaos.slice(0, 3));
    zhiUpper = triFromYaos(zhiYaos.slice(3, 6));
  }
  return {
    yaos,              // [6/7/8/9 × 6] bottom→top
    moving: movingIdxs,
    benLower, benUpper, // trigram indices for 本卦
    zhiLower, zhiUpper, // trigram indices for 之卦 (沒動爻就 == 本卦)
    hasChange: movingIdxs.length > 0,
  };
}

// 依本卦等級、塔羅牌位、問題雜湊，挑出今夜「宜／忌」一組
// 宜/忌使用 hash 的不同 bit 段，讓兩格獨立——避免「不同問題剛好抽到同一組」
function genYiJi(hex, tarot, questionHash) {
  const tier = (hex && hex.c) || '平';
  const yiPool = YI_POOL[tier] || YI_POOL['平'];
  const jiPool = JI_POOL[tier] || JI_POOL['平'];
  const tarotIdx = Math.max(0, TAROT.findIndex(t => t.n === (tarot && tarot.n)));
  const revBoost = (tarot && tarot.reversed) ? 7 : 0;
  const yi = yiPool[(questionHash + tarotIdx + revBoost) % yiPool.length];
  const ji = jiPool[((questionHash >>> 11) + tarotIdx * 3 + revBoost) % jiPool.length];
  return { yi, ji };
}

